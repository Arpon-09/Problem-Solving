import java.util.*;
public class A_El_fucho {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int T = sc.nextInt();

        while (T -->0) {
            int k = sc.nextInt();
            int l = (k-1) * 2;
            System.out.println(l);
        }


    }
}